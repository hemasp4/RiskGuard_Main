# ── Generate test WAV (1.5s voiced speech-like signal) ────────────────────────
sr = 16000
samples = []
for i in range(int(sr * 1.5)):
    ti = i / sr
    envelope = 0.5 + 0.4 * math.sin(2 * math.pi * 4.5 * ti)
    f0 = 150 + 15 * math.sin(2 * math.pi * 0.7 * ti)
    sig = (0.6*math.sin(2*math.pi*f0*ti) + 0.2*math.sin(2*math.pi*f0*2*ti) +
           0.1*math.sin(2*math.pi*f0*3*ti))
    samples.append(max(-32767, min(32767, int(sig * envelope * 20000))))
buf = io.BytesIO()
with wave.open(buf, 'wb') as wf:
    wf.setnchannels(1); wf.setsampwidth(2); wf.setframerate(sr)
    wf.writeframes(struct.pack(f'<{len(samples)}h', *samples))
WAV_BYTES = buf.getvalue()

