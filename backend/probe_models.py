results = []

async def probe(client, label, url, **kwargs):
    try:
        r = await client.post(url, **kwargs, timeout=30)
        body = r.text[:300].encode("ascii", errors="replace").decode("ascii")
        results.append(f"[{r.status_code}] {label}\n    {body[:200]}")
    except Exception as e:
        results.append(f"[ERR] {label}\n    {str(e)[:150]}")

async def main():
    async with httpx.AsyncClient(timeout=35) as c:

        # ── AUDIO: Standard HF inference router ──────────────────────────────
        audio_hdr = {**HEADERS_AUTH, "Content-Type": "audio/wav"}
        ROUTER = "https://router.huggingface.co/hf-inference/models"

        # These are the BEST candidates from our research (0.997 accuracy on V2)
        audio_models = [
            "MelodyMachine/Deepfake-audio-detection-V2",
            "mo-thecreator/Deepfake-audio-detection",
            "Hemgg/Deepfake-audio-detection",
        ]
        for m in audio_models:
            await probe(c, f"AUDIO router/{m}", f"{ROUTER}/{m}",
                        content=WAV_BYTES, headers=audio_hdr)

        # ── AUDIO: Try HF Spaces Gradio REST (no binary; use data URI) ───────
        # Gradio v3 API: POST /run/predict with base64 audio
        spaces = [
            ("Krish1440/ai-voice-detector-api",    "https://krish1440-ai-voice-detector-api.hf.space/run/predict"),
            ("AK-Gaming-92/voice-detection-api",   "https://ak-gaming-92-voice-detection-api.hf.space/run/predict"),
        ]
        b64_wav = "data:audio/wav;base64," + base64.b64encode(WAV_BYTES).decode()
        for name, url in spaces:
            await probe(c, f"SPACE Gradio/{name}", url,
                        json={"data": [{"name": "audio.wav", "data": b64_wav}]},
                        headers=HEADERS_AUTH)

        # ── IMAGE: Standard HF inference router ──────────────────────────────
        img_hdr = {**HEADERS_AUTH, "Content-Type": "image/jpeg"}
        image_models = [
            "Nahrawy/AIorNot",
            "Falconsai/nsfw_image_detection",
            "umm-maybe/AI-image-detector",
            "haywoodSlater/ai-image-detector",
        ]
        for m in image_models:
            await probe(c, f"IMAGE router/{m}", f"{ROUTER}/{m}",
                        content=JPEG_BYTES, headers=img_hdr)

    print(f"\n{'='*70}")
    print(f"Results ({len(results)} probes):")
    print('='*70)
    for r in results:
        print(r)
        print()

    with open("probe_results.log", "w", encoding="utf-8") as f:
        f.write("\n".join(results))
    print("Saved -> probe_results.log")

asyncio.run(main())
